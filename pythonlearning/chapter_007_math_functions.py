import math

pi = 3.14
x = 1
y = 4
z = 2

print(round(pi))
print(math.ceil(pi))        # round up
print(math.floor(pi))       # round down
print((pi*-1))
print(abs(pi*-1))           # absolute value
print(pow(pi, 2))           # raising pi to the power of 2
print(math.sqrt(pi))        # square root
print(max(x,y,z))
print(min(x,y,z))